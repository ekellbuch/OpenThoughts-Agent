#!/usr/bin/env python3
"""Count trajectories and estimate token usage from trajectory.json files.

Recursively scans a trace_jobs directory for trajectory.json files, extracts
token counts from final_metrics, and reports per-trajectory and aggregate stats.

Usage:
    python scripts/analysis/parse_trajectory_tokens.py <trace_jobs_dir>

Example:
    python scripts/analysis/parse_trajectory_tokens.py \
        traces/rl_traces/.../trace_jobs
"""

import argparse
import json
import statistics
import sys
from pathlib import Path


def main():
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("trace_dir", type=Path, help="Root directory to scan for trajectory.json files")
    parser.add_argument("--verbose", "-v", action="store_true", help="Print per-trajectory details")
    args = parser.parse_args()

    if not args.trace_dir.is_dir():
        print(f"Error: {args.trace_dir} is not a directory", file=sys.stderr)
        sys.exit(1)

    traj_files = sorted(args.trace_dir.rglob("trajectory.json"))
    # Exclude continuation/summarization trajectories
    traj_files = [f for f in traj_files if ".cont-" not in f.name and ".summarization" not in str(f)]

    if not traj_files:
        print("No trajectory.json files found.", file=sys.stderr)
        sys.exit(1)

    print(f"Found {len(traj_files)} trajectory files\n")

    prompt_tokens = []
    completion_tokens = []
    cached_tokens = []
    n_steps_list = []
    no_metrics_count = 0

    for traj_path in traj_files:
        try:
            with open(traj_path) as f:
                data = json.load(f)
        except (json.JSONDecodeError, OSError) as e:
            if args.verbose:
                print(f"  SKIP {traj_path.parent.name}: {e}")
            continue

        fm = data.get("final_metrics", {})
        pt = fm.get("total_prompt_tokens")
        ct = fm.get("total_completion_tokens")
        cach = fm.get("total_cached_tokens", 0)
        steps = data.get("steps", [])

        if pt is None and ct is None:
            no_metrics_count += 1
            if args.verbose:
                print(f"  {traj_path.parent.name}: no token metrics (steps={len(steps)})")
            continue

        pt = pt or 0
        ct = ct or 0
        cach = cach or 0

        prompt_tokens.append(pt)
        completion_tokens.append(ct)
        cached_tokens.append(cach)
        n_steps_list.append(len(steps))

        if args.verbose:
            trial_name = traj_path.parent.parent.name
            print(
                f"  {trial_name}: "
                f"prompt={pt:,}  completion={ct:,}  cached={cach:,}  "
                f"steps={len(steps)}"
            )

    n = len(prompt_tokens)
    if n == 0:
        print("No trajectories with token metrics found.")
        sys.exit(0)

    total_prompt = sum(prompt_tokens)
    total_completion = sum(completion_tokens)
    total_cached = sum(cached_tokens)
    total_all = total_prompt + total_completion

    print(f"Trajectories with metrics: {n}")
    if no_metrics_count:
        print(f"Trajectories without metrics: {no_metrics_count}")
    print()

    print("PROMPT TOKENS (input to model)")
    print(f"  Total:   {total_prompt:>12,}")
    print(f"  Min:     {min(prompt_tokens):>12,}")
    print(f"  Avg:     {statistics.mean(prompt_tokens):>12,.0f}")
    print(f"  Median:  {statistics.median(prompt_tokens):>12,.0f}")
    print(f"  Max:     {max(prompt_tokens):>12,}")
    print()

    print("COMPLETION TOKENS (generated by model)")
    print(f"  Total:   {total_completion:>12,}")
    print(f"  Min:     {min(completion_tokens):>12,}")
    print(f"  Avg:     {statistics.mean(completion_tokens):>12,.0f}")
    print(f"  Median:  {statistics.median(completion_tokens):>12,.0f}")
    print(f"  Max:     {max(completion_tokens):>12,}")
    print()

    if any(c > 0 for c in cached_tokens):
        print("CACHED TOKENS (prefix cache hits)")
        print(f"  Total:   {total_cached:>12,}")
        print(f"  Avg:     {statistics.mean(cached_tokens):>12,.0f}")
        cache_rate = total_cached / total_prompt * 100 if total_prompt else 0
        print(f"  Cache hit rate: {cache_rate:.1f}%")
        print()

    print("STEPS PER TRAJECTORY (agent turns)")
    print(f"  Min:     {min(n_steps_list):>12}")
    print(f"  Avg:     {statistics.mean(n_steps_list):>12.1f}")
    print(f"  Median:  {statistics.median(n_steps_list):>12.0f}")
    print(f"  Max:     {max(n_steps_list):>12}")
    print()

    print("=" * 50)
    print("TOTALS")
    print(f"  Prompt tokens:     {total_prompt:>12,}")
    print(f"  Completion tokens: {total_completion:>12,}")
    print(f"  All tokens:        {total_all:>12,}")
    print(f"  Trajectories:      {n:>12}")


if __name__ == "__main__":
    main()
