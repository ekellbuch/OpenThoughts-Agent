def _size_category(num_bytes: int) -> str:
    if num_bytes < 1_000_000:
        return "n<1K"
    if num_bytes < 10_000_000:
        return "1K<n<10K"
    if num_bytes < 100_000_000:
        return "10K<n<100K"
    if num_bytes < 1_000_000_000:
        return "100K<n<1M"
    return "1M<n<10M"


def _repack_snappy(src: Path, dst: Path) -> tuple[int, int]:
    """Rewrite parquet with snappy compression. Returns (n_rows, dst_bytes)."""
    import pyarrow.parquet as pq

    table = pq.read_table(src)
    pq.write_table(table, dst, compression="snappy")
    return table.num_rows, dst.stat().st_size


def _build_readme(repo_name: str, source: str, slug: str, n_rows: int) -> str:
    return README_TEMPLATE.format(
        repo_name=repo_name,
        source=source,
        slug=slug,
        size_category=_size_category(n_rows * 1000),  # rough proxy
        verifier_family=VERIFIER_FAMILY_BY_REPO.get(slug, "see source"),
    )


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument(
        "--output-dir",
        default="data/nemotron_gym/output",
        type=Path,
        help="Directory containing the parquet files",
    )
    ap.add_argument(
        "--dataset",
        action="append",
        help="Specific parquet filename(s) to upload (default: all)",
    )
    ap.add_argument(
        "--dry-run",
        action="store_true",
        help="Repack + write README locally but skip HF upload",
    )
    ap.add_argument(
        "--private",
        action="store_true",
        help="Create repos as private",
    )
    args = ap.parse_args()

    token = os.environ.get("HF_TOKEN")
    if not token and not args.dry_run:
        print("ERROR: HF_TOKEN not set. `source ~/secrets.env` or pass --dry-run", file=sys.stderr)
        return 1

    if not args.dry_run:
        from huggingface_hub import HfApi, create_repo  # noqa: F401
        api = HfApi(token=token)
    else:
        api = None

    selected = set(args.dataset) if args.dataset else None
    summary: list[dict] = []
    for parquet_name, repo_name, source in DATASETS:
        if selected and parquet_name not in selected:
            continue
        src = args.output_dir / parquet_name
        if not src.exists():
            print(f"SKIP {parquet_name} — not found at {src}")
            continue
        slug = repo_name.split("/", 1)[1].removeprefix("nemotron-gym-")
        print(f"\n=== {repo_name}")
        print(f"  source: {src} ({src.stat().st_size:,} bytes)")
        t0 = time.time()
        with tempfile.TemporaryDirectory() as td:
            tmp = Path(td)
            staged_parquet = tmp / "tasks.parquet"
            n_rows, new_size = _repack_snappy(src, staged_parquet)
            print(f"  repacked: {new_size:,} bytes, {n_rows:,} rows ({time.time()-t0:.1f}s)")
            readme = _build_readme(repo_name, source, slug, n_rows)
            (tmp / "README.md").write_text(readme)
            if args.dry_run:
                local_target = args.output_dir / f"{src.stem}.hf"
                local_target.mkdir(exist_ok=True)
                shutil.copy(staged_parquet, local_target / "tasks.parquet")
                (local_target / "README.md").write_text(readme)
                print(f"  [dry-run] wrote to {local_target}")
                summary.append({"repo": repo_name, "rows": n_rows, "uploaded": False})
                continue
            from huggingface_hub import create_repo
            create_repo(
                repo_id=repo_name,
                repo_type="dataset",
                private=bool(args.private),
                token=token,
                exist_ok=True,
            )
            t1 = time.time()
            api.upload_folder(
                folder_path=str(tmp),
                repo_id=repo_name,
                repo_type="dataset",
                commit_message=(
                    "Initial upload — Harbor-format conversion of "
                    f"{source} via OpenThoughts-Agent's data/nemotron_gym adapter"
                ),
            )
            print(f"  uploaded in {time.time()-t1:.1f}s -> https://huggingface.co/datasets/{repo_name}")
            summary.append({"repo": repo_name, "rows": n_rows, "uploaded": True})

    print("\n=== Summary ===")
    for row in summary:
        status = "uploaded" if row["uploaded"] else "dry-run only"
        print(f"  {row['repo']:60s}  {row['rows']:>10,} rows  [{status}]")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
